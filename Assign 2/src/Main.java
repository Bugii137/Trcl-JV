public class Main {
    public static void main(String[] args) {



        //arithmetic operation
        int a = 16;
        int b = 14;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);


        //relational operators
        System.out.println(a == b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a != b);
        System.out.println(a <= b);
        System.out.println(a >= b);


    }
}